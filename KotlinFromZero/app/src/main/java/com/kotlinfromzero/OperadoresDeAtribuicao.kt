package com.kotlinfromzero

fun main() {
    /*
   += soma
   -= subtração
   *= multiplicação
   /= divisão
   %= resto da divisão

    * */
    var x = 5
    val y = 10

    println(x + y)
    println(x - y)
    println(x * y)
    println(x / y)
    println(x % y)
    x += y
    println(x)
    x++
    println(x)
}